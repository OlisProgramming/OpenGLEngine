#define PI 3.1415926536f

// The constant Tau, defined as 2 * Pi.
#define TAU (PI * 2)

#define PI_2 (PI / 2)
#define PI_3 (PI / 3)
#define PI_4 (PI / 4)
#define PI_6 (PI / 6)

#define DEGREES_360		TAU
#define DEGREES_180		PI
